#include <iostream>

int main() {
    // Exemplos de caracteres especiais em C++
    std::cout << "Linha 1\nLinha 2" << std::endl;         // \n: nova linha
    std::cout << "Tab\tEspaco" << std::endl;              // \t: tabulação
    std::cout << "Aspas duplas: \"" << std::endl;         // \": aspas duplas
    std::cout << "Barra invertida: \\" << std::endl;      // \\: barra invertida

    return 0;
}