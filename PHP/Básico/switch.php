<?php
// Estrutura switch em PHP

$dia = 3;

switch ($dia) {
    case 1:
        echo "Domingo<br>";
        break;
    case 2:
        echo "Segunda-feira<br>";
        break;
    case 3:
        echo "Terça-feira<br>";
        break;
    case 4:
        echo "Quarta-feira<br>";
        break;
    case 5:
        echo "Quinta-feira<br>";
        break;
    case 6:
        echo "Sexta-feira<br>";
        break;
    case 7:
        echo "Sábado<br>";
        break;
    default:
        echo "Dia inválido<br>";
        break;
}
?>